public class JavaMath {
    public static void main(String args[]) {

        int a = -5;
        System.out.println("The absolute value of "+a+" is "+Math.abs(a));

        int b = 10;
        int c = 5;

        System.out.println("\nThe max value is "+Math.max(b, c));

        System.out.println("\nThe min value is "+Math.min(b, c));

        System.out.println("\nSquare root of "+b+" is "+Math.sqrt(10));

        System.out.println("\na to the power b is "+Math.pow(a, b));

    }
}
