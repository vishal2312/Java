public class PrimitiveDataTypes {
    public static void main(String args[]) {

        // Byte data type consist value from -128 to 127
        byte b = 23;
        System.out.println("The value of byte variable b is "+b);

        // Short data type consist value from -32k to 32k 
        short s = 23000;
        System.out.println("\nThe value of short variable s is "+s);

        // Int data type consist value from -2b to 2b
        int i = 15172823;
        System.out.println("\nThe value of int variable i is "+i);

        // Long data type consist value from -9q to 9q
        long l = 1508172823;
        System.out.println("\nThe value of long variable l is "+l);

        // Float data type consist value upto 7 decimals
        float f = 5.74647f;
        System.out.println("\nThe value of float variable f is "+f);

        // Double data type consist value upto 16 decimals
        double d = 8.297389784328923;
        System.out.println("\nThe value of double variable d is "+d);

        // Char data type consist of all the character
        char c = 'a';
        System.out.println("\nThe value of char variable c is "+c);

        // Boolean data type consist of only two values true and false
        boolean a = true;
        System.out.println("\nThe value of boolean variable a is "+a);







    }
}
